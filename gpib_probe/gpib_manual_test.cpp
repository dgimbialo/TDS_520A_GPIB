static void pstat(const char* label)
{
    printf("  %-22s ibsta=0x%04X iberr=%d ibcntl=%ld\n",
           label, ibsta, iberr, ibcntl);
}

static void trim(char* buf, long n)
{
    if (n <= 0) return;
    buf[n] = '\0';
    for (long i = n - 1; i >= 0 && (buf[i] == '\n' || buf[i] == '\r' || buf[i] == ' '); --i)
        buf[i] = '\0';
}

// ????????? ???????, ??????????? ?????????
static bool cmd(int ud, const char* c)
{
    printf("\n[WRITE] \"%s\"\n", c);
    ibwrt(ud, (PVOID)c, (long)strlen(c));
    pstat("ibwrt");
    if (ibsta & ERR) { printf("  >>> ??????? ??????!\n"); return false; }
    return true;
}

// ????? + ??????? ASCII ?????????
static bool query(int ud, const char* c, char* resp, int respSize)
{
    printf("\n[QUERY] \"%s\"\n", c);
    ibwrt(ud, (PVOID)c, (long)strlen(c));
    pstat("ibwrt");
    if (ibsta & ERR) { printf("  >>> ??????? ??????!\n"); return false; }

    memset(resp, 0, respSize);
    ibrd(ud, resp, respSize - 1);
    pstat("ibrd");
    if (ibsta & ERR) { printf("  >>> ??????? ???????!\n"); return false; }
    trim(resp, ibcntl);
    printf("  ?????????: \"%s\"\n", resp);
    return true;
}

// ??????? IEEE 488.2 ????????? ????? (#NXXXXXXXX<data>)
static bool read_binary_block(int ud, unsigned char* data, long maxLen, long* outLen)
{
    // ???? 1: ??????? ????? 2 ????? ('#' + ????????? ????)
    printf("\n[BINARY BLOCK] ??????? ????????? (#N)...\n");
    char hdr[3] = {};
    ibrd(ud, hdr, 2);
    pstat("ibrd #N");
    if (ibsta & ERR) { printf("  >>> EABO ?? ?????????!\n"); return false; }
    printf("  hdr[0]=0x%02X('%c') hdr[1]=0x%02X('%c')\n",
           (unsigned char)hdr[0], hdr[0],
           (unsigned char)hdr[1], hdr[1]);

    if (hdr[0] != '#')
    {
        printf("  >>> ?? '#' — ?????? ?? ????! ???????? 0x%02X\n", (unsigned char)hdr[0]);
        return false;
    }
    int nDigits = hdr[1] - '0';
    if (nDigits <= 0 || nDigits > 9)
    {
        printf("  >>> ????????? ????????? ????: %d\n", nDigits);
        return false;
    }

    // ???? 2: ??????? nDigits ???? ???????
    char lenBuf[11] = {};
    ibrd(ud, lenBuf, (long)nDigits);
    pstat("ibrd len");
    if (ibsta & ERR) { printf("  >>> EABO ?? ???????!\n"); return false; }
    lenBuf[nDigits] = '\0';
    long dataLen = atol(lenBuf);
    printf("  ??????? ?????: %ld ????\n", dataLen);

    if (dataLen <= 0 || dataLen > maxLen)
    {
        printf("  >>> ?????????? ???????!\n");
        return false;
    }

    // ???? 3: ??????? ???? ????
    printf("  ??????? %ld ???? ?????...\n", dataLen);
    ibrd(ud, data, dataLen);
    pstat("ibrd data");
    if (ibsta & ERR) { printf("  >>> EABO ?? ?????!\n"); return false; }
    *outLen = ibcntl;
    printf("  ?????????: %ld ????\n", *outLen);

    // ???? 4: trailing newline (?????????? ???????)
    char trail[2] = {};
    ibrd(ud, trail, 1);
    return true;
}

